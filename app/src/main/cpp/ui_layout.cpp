void build_font_atlas(uint8_t *atlas) {
    memset(atlas, 0, FONT_ATLAS_W * FONT_ATLAS_H);
    for (int c = 32; c < 128; ++c) {
        int glyphIndex = c - 32;
        int col = glyphIndex % FONT_COLS;
        int row = glyphIndex / FONT_COLS;
        for (int y = 0; y < FONT_GLYPH_H; ++y) {
            uint8_t bits = font8x8_basic[c][y];
            if (c == 127) {
                bits = 0xFF;
            }
            for (int x = 0; x < FONT_GLYPH_W; ++x) {
                uint8_t on = (bits >> x) & 0x1;
                int atlasX = col * FONT_GLYPH_W + (FONT_GLYPH_W - 1 - x);
                int atlasY = row * FONT_GLYPH_H + (FONT_GLYPH_H - 1 - y);
                atlas[atlasY * FONT_ATLAS_W + atlasX] = on ? 255 : 0;
            }
        }
    }
}

void append_glyph(Vertex *vertices, uint32_t *count, float x, float y,
                  float w, float h, float u0, float v0, float u1, float v1,
                  float screenW, float screenH, bool mirrorX,
                  float r, float g, float b) {
    if (mirrorX) {
        x = screenW - x - w;
    }
    float yFlipped = screenH - y - h;
    float left = (x / screenW) * 2.0f - 1.0f;
    float right = ((x + w) / screenW) * 2.0f - 1.0f;
    float top = 1.0f - (yFlipped / screenH) * 2.0f;
    float bottom = 1.0f - ((yFlipped + h) / screenH) * 2.0f;

    vertices[(*count)++] = VERTEX(left, top,    u0, v0, r, g, b);
    vertices[(*count)++] = VERTEX(right, top,   u1, v0, r, g, b);
    vertices[(*count)++] = VERTEX(right, bottom, u1, v1, r, g, b);
    vertices[(*count)++] = VERTEX(left, top,    u0, v0, r, g, b);
    vertices[(*count)++] = VERTEX(right, bottom, u1, v1, r, g, b);
    vertices[(*count)++] = VERTEX(left, bottom, u0, v1, r, g, b);
}

void append_rect(Vertex *vertices, uint32_t *count,
                 float x, float y, float w, float h,
                 float screenW, float screenH, bool mirrorX,
                 float r, float g, float b) {
    int glyphIndex = 127 - 32;
    int col = glyphIndex % FONT_COLS;
    int row = glyphIndex / FONT_COLS;
    float u0 = (float)(col * FONT_GLYPH_W) / (float)FONT_ATLAS_W;
    float v0 = (float)(row * FONT_GLYPH_H) / (float)FONT_ATLAS_H;
    float u1 = (float)((col + 1) * FONT_GLYPH_W) / (float)FONT_ATLAS_W;
    float v1 = (float)((row + 1) * FONT_GLYPH_H) / (float)FONT_ATLAS_H;
    append_glyph(vertices, count, x, y, w, h, u0, v0, u1, v1,
                 screenW, screenH, mirrorX, r, g, b);
}

void append_border(Vertex *vertices, uint32_t *count,
                   float x, float y, float w, float h, float thickness,
                   float screenW, float screenH, bool mirrorX,
                   float r, float g, float b) {
    append_rect(vertices, count, x, y, w, thickness, screenW, screenH, mirrorX, r, g, b);
    append_rect(vertices, count, x, y + h - thickness, w, thickness, screenW, screenH, mirrorX, r, g, b);
    append_rect(vertices, count, x, y, thickness, h, screenW, screenH, mirrorX, r, g, b);
    append_rect(vertices, count, x + w - thickness, y, thickness, h, screenW, screenH, mirrorX, r, g, b);
}

void append_hline(Vertex *vertices, uint32_t *count,
                  float x, float y, float w, float thickness,
                  float screenW, float screenH, bool mirrorX,
                  float r, float g, float b) {
    append_rect(vertices, count, x, y, w, thickness, screenW, screenH, mirrorX, r, g, b);
}

void append_vline(Vertex *vertices, uint32_t *count,
                  float x, float y, float h, float thickness,
                  float screenW, float screenH, bool mirrorX,
                  float r, float g, float b) {
    append_rect(vertices, count, x, y, thickness, h, screenW, screenH, mirrorX, r, g, b);
}

float draw_string(Vertex *vertices, uint32_t *count, uint32_t capacity,
                  const char *text, float x, float y,
                  float glyphW, float glyphH,
                  float screenW, float screenH, bool mirrorX,
                  float r, float g, float b) {
    for (size_t i = 0; i < strlen(text); ++i) {
        unsigned char c = (unsigned char)text[i];
        if (c < 32 || c > 127) {
            x += glyphW;
            continue;
        }
        int glyphIndex = (int)c - 32;
        int col = glyphIndex % FONT_COLS;
        int row = glyphIndex / FONT_COLS;
        float u0 = (float)(col * FONT_GLYPH_W) / (float)FONT_ATLAS_W;
        float v0 = (float)(row * FONT_GLYPH_H) / (float)FONT_ATLAS_H;
        float u1 = (float)((col + 1) * FONT_GLYPH_W) / (float)FONT_ATLAS_W;
        float v1 = (float)((row + 1) * FONT_GLYPH_H) / (float)FONT_ATLAS_H;
        if (*count + 6 <= capacity) {
            append_glyph(vertices, count, x, y, glyphW, glyphH,
                         u0, v0, u1, v1,
                         screenW, screenH, mirrorX,
                         r, g, b);
        }
        x += glyphW;
    }
    return x;
}

float string_width(const char *text, float glyphW) {
    return (float)strlen(text) * glyphW;
}

float draw_wrapped_text(Vertex *vertices, uint32_t *count, uint32_t capacity,
                        const char *text, float x, float y,
                        float glyphW, float glyphH,
                        float maxWidth,
                        float screenW, float screenH, bool mirrorX,
                        float r, float g, float b) {
    float startX = x;
    float curX = x;
    float curY = y;
    int maxCharsPerLine = (int)(maxWidth / glyphW);
    if (maxCharsPerLine < 1) maxCharsPerLine = 1;

    int charsInLine = 0;
    size_t len = strlen(text);
    for (size_t i = 0; i < len; i++) {
        unsigned char c = (unsigned char)text[i];

        if (charsInLine >= maxCharsPerLine) {
            curY += glyphH + 2.0f;
            curX = startX;
            charsInLine = 0;
        }

        if (c < 32 || c > 127) {
            curX += glyphW;
            charsInLine++;
            continue;
        }
        int glyphIndex = (int)c - 32;
        int col = glyphIndex % FONT_COLS;
        int row = glyphIndex / FONT_COLS;
        float u0 = (float)(col * FONT_GLYPH_W) / (float)FONT_ATLAS_W;
        float v0 = (float)(row * FONT_GLYPH_H) / (float)FONT_ATLAS_H;
        float u1 = (float)((col + 1) * FONT_GLYPH_W) / (float)FONT_ATLAS_W;
        float v1 = (float)((row + 1) * FONT_GLYPH_H) / (float)FONT_ATLAS_H;
        if (*count + 6 <= capacity) {
            append_glyph(vertices, count, curX, curY, glyphW, glyphH,
                         u0, v0, u1, v1,
                         screenW, screenH, mirrorX,
                         r, g, b);
        }
        curX += glyphW;
        charsInLine++;
    }

    return curY + glyphH;
}

uint32_t generate_test_text(Vertex *vertices, uint32_t capacity,
                            const char *text, float x, float y,
                            float densityScale, float screenW, float screenH,
                            float r, float g, float b) {
    uint32_t count = 0;
    float scale = 2.0f * densityScale;
    float glyphW = FONT_GLYPH_W * scale;
    float glyphH = FONT_GLYPH_H * scale;
    draw_string(vertices, &count, capacity, text, x, y,
                glyphW, glyphH, screenW, screenH, false,
                r, g, b);
    return count;
}
