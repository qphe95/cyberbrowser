static inline Vertex make_vertex(float px, float py, float u, float v, float r, float g, float b) {
    Vertex vtx;
    vtx.pos[0] = px; vtx.pos[1] = py;
    vtx.uv[0] = u; vtx.uv[1] = v;
    vtx.color[0] = r; vtx.color[1] = g; vtx.color[2] = b;
    return vtx;
}
#define VERTEX(px, py, u, v, r, g, b) make_vertex(px, py, u, v, r, g, b)
#else
#define VERTEX(px, py, u, v, r, g, b) (Vertex){{px, py}, {u, v}, {r, g, b}}
#endif

